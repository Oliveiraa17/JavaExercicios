package Exercicios;

public class Ex62 {
    public static void main(String[] args) {
        int i = 1;

        System.out.println("Números ímpares de 1 a 50:");

        do {
            System.out.print(i + " ");
            i += 2;
        } while (i <= 50);

        System.out.println();
    }
}
